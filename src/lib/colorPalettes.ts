const STORAGE_KEY = 'kcs-color-palette';

const OLD_KEY_MAP: Record<string, PaletteKey> = {
  forest: 'ember',
  ocean: 'slate',
  amethyst: 'cobalt',
  steel: 'graphite',
  crimson: 'midnight',
  teal: 'ivory',
  copper: 'titanium',
  sage: 'ivory',
  bronze: 'titanium',
  charcoal: 'midnight',
};

export function getActivePalette(): PaletteKey {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (!stored) return 'ember';
    if (stored in OLD_KEY_MAP) return OLD_KEY_MAP[stored];
    if (palettes.some((p) => p.key === stored)) return stored as PaletteKey;
  } catch {}
  return 'ember';
}

const LIGHT_PALETTES: PaletteKey[] = ['ivory', 'pearl', 'linen'];

const ALL_PALETTE_VARS = [
  '--background', '--foreground', '--card', '--card-foreground',
  '--popover', '--popover-foreground', '--primary', '--primary-foreground',
  '--secondary', '--secondary-foreground', '--muted', '--muted-foreground',
  '--accent', '--accent-foreground', '--destructive', '--destructive-foreground',
  '--success', '--success-foreground', '--warning', '--warning-foreground',
  '--border', '--input', '--ring', '--radius',
  '--chart-1', '--chart-2', '--chart-3', '--chart-4', '--chart-5',
  '--sidebar-background', '--sidebar-foreground', '--sidebar-primary',
  '--sidebar-primary-foreground', '--sidebar-accent', '--sidebar-accent-foreground',
  '--sidebar-border', '--sidebar-ring',
];

export function applyPalette(key: PaletteKey) {
  const palette = palettes.find((p) => p.key === key);
  if (!palette) return;
  const root = document.documentElement;
  // Clear all palette vars first to prevent stale overrides from previous palette
  ALL_PALETTE_VARS.forEach((prop) => root.style.removeProperty(prop));
  Object.entries(palette.variables).forEach(([prop, value]) => {
    root.style.setProperty(prop, value);
  });
  // Set color-scheme so native date/time pickers match the palette
  const isLight = LIGHT_PALETTES.includes(key);
  root.style.setProperty('color-scheme', isLight ? 'light' : 'dark');
  try {
    localStorage.setItem(STORAGE_KEY, key);
  } catch {}
  // Sync to cloud
  import('@/hooks/useSettingsSync').then(({ triggerSettingsSync }) => triggerSettingsSync()).catch(() => {});
}
