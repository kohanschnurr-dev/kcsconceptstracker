const STORAGE_KEY = 'kcs-color-palette';

const OLD_KEY_MAP: Record<string, PaletteKey> = {
  forest: 'ember',
  ocean: 'slate',
  amethyst: 'cobalt',
  steel: 'graphite',
  crimson: 'charcoal',
  teal: 'sage',
  copper: 'bronze',
  midnight: 'onyx',
};

export function getActivePalette(): PaletteKey {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (!stored) return 'ember';
    if (stored in OLD_KEY_MAP) return OLD_KEY_MAP[stored];
    if (palettes.some((p) => p.key === stored)) return stored as PaletteKey;
  } catch {}
  return 'ember';
}

export function applyPalette(key: PaletteKey) {
  const palette = palettes.find((p) => p.key === key);
  if (!palette) return;
  const root = document.documentElement;
  Object.entries(palette.variables).forEach(([prop, value]) => {
    root.style.setProperty(prop, value);
  });
  try {
    localStorage.setItem(STORAGE_KEY, key);
  } catch {}
}
